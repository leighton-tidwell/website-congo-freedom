                    <h3>
                        Orientation video about DR Congo problems
                    </h3>
                    <p>
                        This film is titled &ldquo;Crisis in the Congo: Uncovering the Truth&rdquo; produced by &ldquo;Friends of the Congo&rdquo;
                        in 2011 <a data-ajax="false" href="http://congojustice.org/">http://congojustice.org/</a> It is a short 26 minute video documentary version of a feature
                        length film intended for the future. By interviewing many world experts, the film explores the
                        role that the United States, Belgium, Europe, Rwanda, Uganda, Burundi, other African states,
                        billionaires and multinational corporations seeking wealth - have all played a role in triggering
                        the greatest humanitarian crisis on earth since World War Two, yet few in the world know
                        anything about it. To update the facts of this film &ndash; the dead alone in Congo, Rwanda, and
                        Burundi are more than one million more than all wars on earth added together since World War
                        Two. It is horrific genocide, war crimes and crimes against humanity. It is the rape capitol of
                        the world. Child soldiers. The greatest theft of the Congo&rsquo;s resources since Belgium&rsquo;s Leopold
                        II slaughtered half the population to get it from 1885 to 1908. The Congo is occupied by
                        Rwandans who control the destiny of 81 million Congolese. There is no freedom. There are no
                        human rights in Congo where human lives are worth less than chickens. Enough. The film is a
                        call to conscience and action. Friends of the Congo holds the rights to this film, and authorizes
                        it to be freely distributed, duplicated and screened on the web, in public places, or at home.
                    </p>
                    <hr>
                    <h3>
                        Other DR Congo Media
                    </h3>
                    <p>
                        Who are these wonderful Congolese we are trying to help? See this CBS &ldquo;This Morning&rdquo;
                        interview (click here) <a data-ajax="false" href="https://www.youtube.com/watch?v=lROK5pu5jxg">https://www.youtube.com/watch?v=lROK5pu5jxg</a> of the late reporter Bob
                        Simon and famed musician Wynton Marsalis, who discuss these impoverished Congolese who
                        find a way to make music during chaos. &ldquo;The Kimbanguist Symphony Orchestra (OSK) is
                        based in Kinshasa, the capital of the Democratic Republic of Congo (DRC). Having gone
                        through decades of war, the DRC is a fragile country and among one of the poorest in the world.
                        But those challenges have never deterred the 200-member orchestra and choir from playing
                        and composing beautiful classical music.&rdquo; CBS.
                    </p>
                    <p>
                        After Bob Simon passed away, the following tribute was aired about him and Congolese. View
                        &ldquo;Remembering Bob Simon: Joy in the Congo&rdquo;
                        <a data-ajax="false" href="https://www.youtube.com/watch?v=2kOMccvaTb0">https://www.youtube.com/watch?v=2kOMccvaTb0</a> This program earned a prestigious Peabody
                        Award <a data-ajax="false" href="http://www.peabodyawards.com/stories/story/60-minutes-joy-in-the-congo-72nd-award-acceptance">http://www.peabodyawards.com/stories/story/60-minutes-joy-in-the-congo-72nd-award-acceptance</a>
                    </p>
                    <p>
                        Man cannot live without water for much longer than 3 days. But using &ldquo;unsafe&rdquo; deadly water will
                        quickly kill and injure, too. Among 81.3 million Congolese, only about 35 million (43%) live in
                        urban areas, while 46 million (57%) live in rural areas. Of urban dwellers, 6.7 million (19%)
                        obtain water from &ldquo;unsafe sources,&rdquo; which is the equivalent of an urban population about the
                        size of Los Angeles and Chicago combined. Congolese are drinking water from unsafe
                        streams, water puddles, and other sources.
                    </p>
                    <p>
                        Here is a video of a rural DRC woman who contracted &ldquo;river blindness&rdquo; by drinking from a
                        nearby river due to no other option (click on video link) <a data-ajax="false" href="https://www.water4.org/2015-end-of-year/?gclid=CJO1mL3butICFZIvgQodQzMH6w">https://www.water4.org/2015-end-of-year/?gclid=CJO1mL3butICFZIvgQodQzMH6w</a> Entire families, including children, are victims of
                        unsafe water and ineffective sanitation. Congolese are injured and die needlessly.
                    </p>
                    <p>
                        28.3 million urban residents either have access to &ldquo;improved&rdquo; drinking water from a municipal treated water system,
                        or they buy commercial bottled water - if one can afford to buy a liter of water for $1 in a DRC economy where at least 63% or
                        52.1 million Congolese live below the poverty line (CIA 2016). Unemployment numbers are as low as 58% to as high as 73%, and those
                        who do work only earn on average one dollar U.S. a day. Affording water is a problemeven if one has access. See 
                        <a data-ajax="false" href="https://www.cia.gov/library/publications/the-world-factbook/geos/cg.html">https://www.cia.gov/library/publications/the-world-factbook/geos/cg.html</a> 
                        and <a data-ajax="false" href="https://thewaterproject.org/water-crisis/water-in-crisis-congo">https://thewaterproject.org/water-crisis/water-in-crisis-congo</a>
                    </p>
                    <p>
                        The rural problem where most people live is worse. Based on CIA statistics, 38.7 million Congolese 
                        6.7 million urban and 32 million rural) do not have &ldquo;safe&rdquo; drinking water sources. That&rsquo;s
                        the entire population of California or 4.6 times the population of New York City! Thus, this population is 
                        in constant danger of water borne diseases, illnesses, and death that would be avoidable by using safe water
                        and effective sanitation. However, a report by the World Bank Water and Sanitation Program (WSP) states that 
                        more like 50 million Congolese are without safe water.
                    </p>
                    <p>
                        The World Health Organization (WHO) states that an improved drinking-water source is defined as
                        &ldquo;one that, by nature of its construction or through active intervention, is likely to be 
                        protected from outside contamination, in particular from contamination with fecal matter.&rdquo; 
                        <a data-ajax="false" href="https://www.wssinfo.org/definitions-methods/">https://www.wssinfo.org/definitions-methods/</a>
                    </p>
                    <p>
                        Providing safe water and effective sanitation are basic responsibilities of competent government. 
                        Where governments fail to provide this essential for life, there is a role for non-government organizations (NGO non-profit charities).
                        They have technical solutions, if necessary funding was in place and there was a commitment by government to permit implementation, 
                        and if nonprofits are permitted to implement such programs absent of corruption that robs the intent of the program. A few nonprofits 
                        interested in this issue, besides Congo Freedom Alliance, are: &ldquo;Water&rdquo; backed by the actor Matt Damon <a data-ajax="false" href="http://water.org/">http://water.org/</a> 
                        and view video interview (click on video link) <a data-ajax="false" href="http://www.cbsnews.com/videos/matt-damon-and-gary-whiteon-worldwide-initiative-for-clean-water/">http://www.cbsnews.com/videos/matt-damon-and-gary-whiteon-worldwide-initiative-for-clean-water/</a> and see 
                        <a data-ajax="false" href="http://haute-giving.com/?p=2573">http://haute-giving.com/?p=2573</a> and see &ldquo;The Water Project&rdquo; <a data-ajax="false" href="https://thewaterproject.org/?gclid=CK_flZ3RutICFUw6gQodwpELTw">https://thewaterproject.org/?gclid=CK_flZ3RutICFUw6gQodwpELTw</a>
                    </p>
                    <p>
                        The lack of safe water and good sanitation is caused by government incompetence, as the DRC is blessed by resource wealth
                        equal to the GDPs of the USA, France, Great Britain, and Germany combined. While some Congolese and foreigners have become
                        wealthy by exploiting these valuables, the Congolese people are rated at the bottom of the world&rsquo;s poor, along with 
                        two other countries. Tragic. Why? Among 196 countries, the DRC government, responsible for causing the nationwide violence 
                        for more than a generation, is rated among a group of 19 of the most corrupt governments on earth, 
                        <a data-ajax="false" href="http://www.transparency.org/news/feature/corruption_perceptions_index_2016">http://www.transparency.org/news/feature/corruption_perceptions_index_2016</a> 
                        The DRC government is ranked among top 8 &ldquo;Failed State&rdquo; dictatorships on earth <a data-ajax="false" href="http://fsi.fundforpeace.org">http://fsi.fundforpeace.org</a> Thus, 
                        we should not expect a different outcome from bad corrupt management.</p>
